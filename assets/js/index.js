//  RENDER
function renderProjects(category) {
  const track = document.getElementById("projectsTrack");
  const empty = document.getElementById("projectsEmpty");
  const filtered = projectsData.filter((p) => p.category === category);

  document.querySelectorAll(".project-card.flipped").forEach((c) =>
    c.classList.remove("flipped")
  );

  if (!filtered.length) {
    track.innerHTML = "";
    empty.style.display = "flex";
    return;
  }

  empty.style.display = "none";
  track.innerHTML = filtered.map((p, i) => buildCard(p, i + 1)).join("");
  renderModals(filtered);


  filtered.forEach((p) => {
    const card = document.getElementById("card-" + p.id);

    // Front face 
    const front = card.querySelector(".project-front");
    if (front) {
      front.addEventListener("click", () => {
        card.classList.add("flipped");
      });
    }

    // Back flip-hint bar 
    const flipBack = card.querySelector(".project-flip-back");
    if (flipBack) {
      flipBack.addEventListener("click", (e) => {
        e.stopPropagation();
        card.classList.remove("flipped");
      });
    }

    // More Details button 
    const detailsBtn = document.getElementById("btn-details-" + p.id);
    if (detailsBtn) {
      detailsBtn.addEventListener("click", (e) => {
        e.stopPropagation();
        openModal(p.id);
      });
    }

    // Visit Site link 
    const visitBtn = document.getElementById("btn-visit-" + p.id);
    if (visitBtn) {
      visitBtn.addEventListener("click", (e) => {
        e.stopPropagation();
      });
    }
  });
}

