(function initLoader() {
  const overlay = document.getElementById('loaderOverlay');
  const bar = document.getElementById('loaderBarFill');
  const percent = document.getElementById('loaderPercent');
  const logoFill = document.getElementById('loaderLogoFill');
  const hero = document.querySelector('.hero') || document.body;
  if (!overlay) return;

  document.body.style.overflow = 'hidden';

  //  Floating code snippets 
  const snippets = [
    '<div>', '</div>', '{ }', '( )', '=>', 'const', 'return', '.map()',
    'async', 'await', '</>', '[ ]', 'npm run', 'git push', 'useState()',
    'flex', '.then()', '#root', 'px', 'rem', 'border:', 'z-index:', '<img />',
    'onClick', 'className', 'export', 'import', '...spread', 'display:', '<nav>'
  ];

  const W = window.innerWidth;
  const H = window.innerHeight;
  const CX = W / 2;
  const CY = H / 2;
  const SAFE_W = 280;
  const SAFE_H = 160;
  const floats = [];

  snippets.forEach(text => {
    const el = document.createElement('div');
    el.className = 'hero-float' + (Math.random() > 0.6 ? ' dim' : '');
    el.textContent = text;
    el.style.position = 'fixed';
    el.style.zIndex = '99998';
    el.style.pointerEvents = 'none';
    overlay.appendChild(el);

    let x, y, tries = 0;
    do {
      x = Math.random() * (W - 120);
      y = Math.random() * (H - 40);
      tries++;
    } while (
      tries < 20 &&
      Math.abs(x + 60 - CX) < SAFE_W &&
      Math.abs(y + 20 - CY) < SAFE_H
    );

    el.style.left = x + 'px';
    el.style.top = y + 'px';

    const speed = 0.15 + Math.random() * 0.25;
    const angle = Math.random() * Math.PI * 2;

    floats.push({
      el, x, y,
      driftX: Math.cos(angle) * speed,
      driftY: Math.sin(angle) * speed,
      rot: Math.random() * 360,
      rotSpeed: (Math.random() - 0.5) * 0.03
    });
  });

  let rafId;
  function tickFloats() {
    floats.forEach(f => {
      f.x += f.driftX;
      f.y += f.driftY;
      f.rot += f.rotSpeed;
      const w = f.el.offsetWidth || 60;
      const h = f.el.offsetHeight || 24;
      if (f.x < -w)  f.x = W + 4;
      if (f.x > W + 4) f.x = -w;
      if (f.y < -h)  f.y = H + 4;
      if (f.y > H + 4) f.y = -h;
      f.el.style.left = f.x + 'px';
      f.el.style.top  = f.y + 'px';
      f.el.style.transform = `rotate(${f.rot}deg)`;
    });
    rafId = requestAnimationFrame(tickFloats);
  }
  tickFloats();

  //  Progress counter 
  let current = 0;
  const duration = 2200;
  const interval = 30;
  const steps = duration / interval;
  const increment = 100 / steps;

  const timer = setInterval(() => {
    current = Math.min(current + increment + (Math.random() * 1.5), 100);
    const rounded = Math.floor(current);

    bar.style.width = rounded + '%';
    percent.textContent = rounded + '%';
    if (logoFill) logoFill.style.width = rounded + '%';

    if (current >= 100) {
      clearInterval(timer);
      bar.style.width = '100%';
      percent.textContent = '100%';
      if (logoFill) logoFill.style.width = '100%';

      setTimeout(() => {
        cancelAnimationFrame(rafId);
        overlay.classList.add('hide');
        document.body.style.overflow = '';
      }, 400);
    }
  }, interval);
})();